extension UIColor
{
   static var tint: UIColor {
      return UIColor(red: 70.0/255, green: 233.0/255, blue: 33.0/255, alpha: 1)
   }
   
   static var appGray:      UIColor { return UIColor(white: 0.2, alpha: 1) }
   
   static var appGreen:      UIColor {  return UIColor(red: 70/255, green: 233/255, blue: 33/255, alpha: 1) }
   static var appBlue:      UIColor {  return UIColor(red: 0, green: 150/255, blue: 1, alpha: 1) }
}
